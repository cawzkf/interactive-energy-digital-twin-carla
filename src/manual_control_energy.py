def game_loop(args):
    """
    Loop principal do pygame.

    Diferenças em relação ao original:
    1. Instancia EnergyClient com a URL do energy_service
    2. Injeta o cliente no HUD via hud.set_energy_client()
    3. O restante do loop é idêntico ao original
    """
    pygame.init()
    pygame.font.init()
    world = None
    original_settings = None

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(20.0)
        sim_world = client.get_world()

        if args.sync:
            original_settings = sim_world.get_settings()
            settings = sim_world.get_settings()
            if not settings.synchronous_mode:
                settings.synchronous_mode = True
                settings.fixed_delta_seconds = 0.05
            sim_world.apply_settings(settings)
            traffic_manager = client.get_trafficmanager(args.tm_port)
            traffic_manager.set_synchronous_mode(True)

        if args.autopilot and not sim_world.get_settings().synchronous_mode:
            print("WARNING: You are currently in asynchronous mode.")

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)
        display.fill((0, 0, 0))
        pygame.display.flip()

        hud = HUD(args.width, args.height)

        # --- Integração energética ---
        # Instancia o cliente HTTP e o injeta no HUD.
        # O EnergyClient já inicia o polling em background automaticamente.
        energy_client = EnergyClient(base_url=args.energy_url)
        hud.set_energy_client(energy_client)

        world = World(sim_world, hud, args)
        controller = KeyboardControl(world, args.autopilot, args.tm_port)

        if args.sync:
            sim_world.tick()
        else:
            sim_world.wait_for_tick()

        clock = pygame.time.Clock()

        while True:
            if args.sync:
                sim_world.tick()
            clock.tick_busy_loop(60)
            if controller.parse_events(client, world, clock, args.sync):
                return
            world.tick(clock)
            world.render(display)
            pygame.display.flip()

    finally:
        if original_settings:
            sim_world.apply_settings(original_settings)
        if world is not None and world.recording_enabled:
            client.stop_recorder()
        if world is not None:
            world.destroy()
        pygame.quit()


