# URL base do energy_service. Pode ser sobrescrita via argumento --energy-url.
ENERGY_SERVICE_URL = "http://localhost:8000"

# Intervalo de polling em segundos.
# 0.1s = 10Hz — compatível com o fixed_dt=0.1 do CARLA no main.py.
ENERGY_POLL_INTERVAL = 0.1


class EnergyClient:
    """
    Cliente HTTP que faz polling periódico no energy_service.

    Roda em uma thread daemon separada para não bloquear o loop pygame.
    A cada ENERGY_POLL_INTERVAL segundos, faz GET /energy/state e
    armazena o resultado em _data, protegido por threading.Lock.

    O HUD lê _data a cada frame via get_data() — leitura nunca bloqueia
    porque o lock é adquirido apenas durante a cópia do dict.
    """

    def __init__(self, base_url: str = ENERGY_SERVICE_URL):
        self._base_url = base_url.rstrip("/")
        self._data: dict = {}
        self._lock = threading.Lock()
        self._connected = False
        self._session = requests.Session()

        # Inicia thread de polling imediatamente
        self._thread = threading.Thread(
            target=self._poll_loop,
            name="energy-poller",
            daemon=True,  # encerra junto com o processo principal
        )
        self._thread.start()

    def _poll_loop(self) -> None:
        """
        Loop de polling que roda na thread daemon.

        Tenta fazer GET /energy/state a cada ENERGY_POLL_INTERVAL segundos.
        Em caso de falha (serviço indisponível, timeout), mantém o último
        dado válido e marca _connected = False — o HUD exibe "Connecting...".

        Usa timeout=0.05s para garantir que a thread não bloqueie por mais
        de 50ms, preservando a responsividade do HUD.
        """
        while True:
            try:
                resp = self._session.get(
                    f"{self._base_url}/energy/state",
                    timeout=0.05,
                )
                if resp.status_code == 200:
                    with self._lock:
                        self._data = resp.json()
                        self._connected = True
                else:
                    # 503 = serviço disponível mas CARLA ainda não enviou tick
                    with self._lock:
                        self._connected = False

            except requests.RequestException:
                with self._lock:
                    self._connected = False

            time.sleep(ENERGY_POLL_INTERVAL)

    def get_data(self) -> dict:
        """
        Retorna uma cópia do último estado energético recebido.

        Thread-safe. Chamado pelo HUD a cada frame do pygame.
        Retorna dict vazio enquanto nenhum dado foi recebido ainda.
        """
        with self._lock:
            return dict(self._data)

    def is_connected(self) -> bool:
        """Retorna True se o último polling foi bem-sucedido."""
        with self._lock:
            return self._connected


