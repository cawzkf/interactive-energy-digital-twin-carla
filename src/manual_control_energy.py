# Separador visual entre o bloco padrão do CARLA e o bloco energético.
_ENERGY_SEPARATOR = '--- Energy Digital Twin ---'

# Cor do painel energético: amarelo âmbar para destacar do HUD padrão (branco).
_ENERGY_COLOR = (255, 200, 0)

# Cor de status quando o serviço está desconectado: vermelho.
_ENERGY_OFFLINE_COLOR = (255, 80, 80)


class HUD(object):
    """
    HUD estendido do CARLA.

    Mantém toda a lógica original intacta.
    Adiciona:
    - Referência ao EnergyClient injetado via set_energy_client()
    - Bloco de linhas energéticas em _energy_text, atualizado a cada tick()
    - Renderização do bloco energético em cor âmbar à direita do HUD padrão
    """

    def __init__(self, width, height):
        self.dim = (width, height)
        font = pygame.font.Font(pygame.font.get_default_font(), 20)
        font_name = 'courier' if os.name == 'nt' else 'mono'
        fonts = [x for x in pygame.font.get_fonts() if font_name in x]
        default_font = 'ubuntumono'
        mono = default_font if default_font in fonts else fonts[0]
        mono = pygame.font.match_font(mono)
        self._font_mono = pygame.font.Font(mono, 12 if os.name == 'nt' else 14)
        self._notifications = FadingText(font, (width, 40), (0, height - 40))
        self.help = HelpText(pygame.font.Font(mono, 16), width, height)
        self.server_fps = 0
        self.frame = 0
        self.simulation_time = 0
        self._show_info = True
        self._info_text = []
        self._server_clock = pygame.time.Clock()
        self._show_ackermann_info = False
        self._ackermann_control = carla.VehicleAckermannControl()

        # --- Extensão energética ---
        # EnergyClient injetado após construção via set_energy_client().
        # Enquanto None, o bloco energético exibe "Service not configured".
        self._energy_client: EnergyClient | None = None
        self._energy_text: list[tuple[str, tuple]] = []

    def set_energy_client(self, client: EnergyClient) -> None:
        """
        Injeta o EnergyClient no HUD.

        Chamado pelo game_loop() após instanciar EnergyClient.
        Desacopla o HUD da construção do cliente HTTP.
        """
        self._energy_client = client

    def on_world_tick(self, timestamp):
        self._server_clock.tick()
        self.server_fps = self._server_clock.get_fps()
        self.frame = timestamp.frame
        self.simulation_time = timestamp.elapsed_seconds

    def tick(self, world, clock):
        """
        Atualiza _info_text (bloco CARLA padrão) e _energy_text (bloco energético).
        Chamado uma vez por frame no game_loop.
        """
        self._notifications.tick(world, clock)
        if not self._show_info:
            return

        # --- Bloco padrão do CARLA (inalterado) ---
        t = world.player.get_transform()
        v = world.player.get_velocity()
        c = world.player.get_control()
        compass = world.imu_sensor.compass
        heading = 'N' if compass > 270.5 or compass < 89.5 else ''
        heading += 'S' if 90.5 < compass < 269.5 else ''
        heading += 'E' if 0.5 < compass < 179.5 else ''
        heading += 'W' if 180.5 < compass < 359.5 else ''
        colhist = world.collision_sensor.get_collision_history()
        collision = [colhist[x + self.frame - 200] for x in range(0, 200)]
        max_col = max(1.0, max(collision))
        collision = [x / max_col for x in collision]
        vehicles = world.world.get_actors().filter('vehicle.*')
        self._info_text = [
            'Server:  % 16.0f FPS' % self.server_fps,
            'Client:  % 16.0f FPS' % clock.get_fps(),
            '',
            'Vehicle: % 20s' % get_actor_display_name(world.player, truncate=20),
            'Map:     % 20s' % world.map.name.split('/')[-1],
            'Simulation time: % 12s' % datetime.timedelta(seconds=int(self.simulation_time)),
            '',
            'Speed:   % 15.0f km/h' % (3.6 * math.sqrt(v.x**2 + v.y**2 + v.z**2)),
            u'Compass: % 17.0f\N{DEGREE SIGN} % 2s' % (compass, heading),
            'Accelero: (%5.1f,%5.1f,%5.1f)' % (world.imu_sensor.accelerometer),
            'Gyroscop: (%5.1f,%5.1f,%5.1f)' % (world.imu_sensor.gyroscope),
            'Location: % 20s' % ('(% 5.1f, % 5.1f)' % (t.location.x, t.location.y)),
            'GNSS:    % 24s' % ('(% 2.6f, % 3.6f)' % (world.gnss_sensor.lat, world.gnss_sensor.lon)),
            'Height:  % 18.0f m' % t.location.z,
            '',
        ]
        if isinstance(c, carla.VehicleControl):
            self._info_text += [
                ('Throttle:', c.throttle, 0.0, 1.0),
                ('Steer:   ', c.steer, -1.0, 1.0),
                ('Brake:   ', c.brake, 0.0, 1.0),
                ('Reverse:', c.reverse),
                ('Hand brake:', c.hand_brake),
                ('Manual:', c.manual_gear_shift),
                'Gear:        %s' % {-1: 'R', 0: 'N'}.get(c.gear, c.gear),
            ]
        if self._show_ackermann_info:
            self._info_text += [
                '',
                'Ackermann Controller:',
                '  Target speed: % 8.0f km/h' % (3.6 * self._ackermann_control.speed),
            ]
        self._info_text += [
            '',
            'Collision:',
            collision,
            '',
            'Number of vehicles: % 8d' % len(vehicles),
        ]
        if len(vehicles) > 1:
            self._info_text += ['Nearby vehicles:']
            distance = lambda l: math.sqrt(
                (l.x - t.location.x)**2 + (l.y - t.location.y)**2 + (l.z - t.location.z)**2)
            vehicles = [(distance(x.get_location()), x) for x in vehicles if x.id != world.player.id]
            for d, vehicle in sorted(vehicles, key=lambda vehicles: vehicles[0]):
                if d > 200.0:
                    break
                vehicle_type = get_actor_display_name(vehicle, truncate=22)
                self._info_text.append('% 4dm %s' % (d, vehicle_type))

        # --- Bloco energético ---
        # Atualizado a cada tick com os dados mais recentes do EnergyClient.
        # Cada item é uma tupla (texto, cor) para renderização colorida.
        self._energy_text = self._build_energy_text()

    def _build_energy_text(self) -> list[tuple[str, tuple]]:
        """
        Constrói a lista de linhas do bloco energético.

        Retorna lista de (str, cor_rgb).
        Chamado internamente pelo tick() a cada frame.
        """
        lines = []

        # Cabeçalho do bloco
        lines.append((_ENERGY_SEPARATOR, _ENERGY_COLOR))

        if self._energy_client is None:
            lines.append(('Service not configured', _ENERGY_OFFLINE_COLOR))
            return lines

        if not self._energy_client.is_connected():
            lines.append(('Status:  Connecting...', _ENERGY_OFFLINE_COLOR))
            return lines

        data = self._energy_client.get_data()

        if not data:
            lines.append(('Status:  Waiting for data...', _ENERGY_OFFLINE_COLOR))
            return lines

        # Converte Joules para kWh para exibição mais legível
        # 1 Wh = 3600 J  →  1 kWh = 3_600_000 J
        energy_kwh = data.get('mech_energy_total', 0.0) / 3_600_000.0

        lines += [
            ('Status:  Online', _ENERGY_COLOR),
            ('', _ENERGY_COLOR),
            ('Power:   % 10.1f  W'   % data.get('power', 0.0),              _ENERGY_COLOR),
            ('Energy:  % 10.4f kWh'  % energy_kwh,                           _ENERGY_COLOR),
            ('SoC:     % 10.1f  %%'  % (data.get('soc', 0.0) * 100),         _ENERGY_COLOR),
            ('Dist:    % 10.1f  m'   % data.get('distance_total', 0.0),      _ENERGY_COLOR),
            ('AvgPwr:  % 10.1f  W'   % data.get('avg_power', 0.0),           _ENERGY_COLOR),
            ('Consump: % 10.6f Wh/m' % data.get('specific_consumption', 0.0), _ENERGY_COLOR),
            ('Autonomy:% 10.1f  s'   % data.get('estimated_autonomy', 0.0),  _ENERGY_COLOR),
        ]
        return lines

    def show_ackermann_info(self, enabled):
        self._show_ackermann_info = enabled

    def update_ackermann_control(self, ackermann_control):
        self._ackermann_control = ackermann_control

    def toggle_info(self):
        self._show_info = not self._show_info

    def notification(self, text, seconds=2.0):
        self._notifications.set_text(text, seconds=seconds)

    def error(self, text):
        self._notifications.set_text('Error: %s' % text, (255, 0, 0))

    def render(self, display):
        """
        Renderiza o HUD completo.

        Painel esquerdo  (x=0)   : bloco padrão CARLA (branco) — inalterado.
        Painel direito   (x=230) : bloco energético (amarelo âmbar).
        """
        if not self._show_info:
            return

        # --- Painel esquerdo: bloco padrão (inalterado) ---
        info_surface = pygame.Surface((220, self.dim[1]))
        info_surface.set_alpha(100)
        display.blit(info_surface, (0, 0))
        v_offset = 4
        bar_h_offset = 100
        bar_width = 106
        for item in self._info_text:
            if v_offset + 18 > self.dim[1]:
                break
            if isinstance(item, list):
                if len(item) > 1:
                    points = [(x + 8, v_offset + 8 + (1.0 - y) * 30) for x, y in enumerate(item)]
                    pygame.draw.lines(display, (255, 136, 0), False, points, 2)
                item = None
                v_offset += 18
            elif isinstance(item, tuple):
                if isinstance(item[1], bool):
                    rect = pygame.Rect((bar_h_offset, v_offset + 8), (6, 6))
                    pygame.draw.rect(display, (255, 255, 255), rect, 0 if item[1] else 1)
                else:
                    rect_border = pygame.Rect((bar_h_offset, v_offset + 8), (bar_width, 6))
                    pygame.draw.rect(display, (255, 255, 255), rect_border, 1)
                    f = (item[1] - item[2]) / (item[3] - item[2])
                    if item[2] < 0.0:
                        rect = pygame.Rect((bar_h_offset + f * (bar_width - 6), v_offset + 8), (6, 6))
                    else:
                        rect = pygame.Rect((bar_h_offset, v_offset + 8), (f * bar_width, 6))
                    pygame.draw.rect(display, (255, 255, 255), rect)
                item = item[0]
            if item:
                surface = self._font_mono.render(item, True, (255, 255, 255))
                display.blit(surface, (8, v_offset))
                v_offset += 18

        # --- Painel direito: bloco energético ---
        if self._energy_text:
            energy_surface = pygame.Surface((240, self.dim[1]))
            energy_surface.set_alpha(100)
            display.blit(energy_surface, (230, 0))
            e_offset = 4
            for text, color in self._energy_text:
                if e_offset + 18 > self.dim[1]:
                    break
                if text:  # linha vazia: só avança offset
                    surface = self._font_mono.render(text, True, color)
                    display.blit(surface, (238, e_offset))
                e_offset += 18

        self._notifications.render(display)
        self.help.render(display)


